package org.example.core;


/**
 * Interface Executable.
 * Purpose: Defines a common interface for all patterns and principles.
 */
public interface Executable {
    /**
     * Execute the example implementation.
     */
    void execute();

    /**
     * Provide a description of the pattern or principle.
     *
     * @return Detailed description.
     */
    String getDescription();
}
